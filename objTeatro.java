public class objTeatro {
    private int Precio;
    private int Fila;
    private int Numero;
    public objTeatro(int precio, int fila, int numero) {
        Precio = precio;
        Fila = fila;
        Numero = numero;
    }
    public int getPrecio() {
        return Precio;
    }
    public void setPrecio(int precio) {
        Precio = precio;
    }
    public int getFila() {
        return Fila;
    }
    public void setFila(int fila) {
        Fila = fila;
    }
    public int getNumero() {
        return Numero;
    }
    public void setNumero(int numero) {
        Numero = numero;
    }

    
}

